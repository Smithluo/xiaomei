<?php

namespace backend\controllers;

use common\helper\DateTimeHelper;
use common\models\AdPosition;
use kartik\grid\EditableColumnAction;
use Yii;
use common\models\Ad;
use common\models\AdSearch;
use yii\filters\AccessControl;
use yii\helpers\ArrayHelper;
use yii\web\Controller;
use yii\web\NotFoundHttpException;
use yii\filters\VerbFilter;

/**
 * AdController implements the CRUD actions for Ad model.
 */
class AdController extends Controller
{
    /**
     * @return array
     */
    public function actions()
    {
        return ArrayHelper::merge(parent::actions(),
        [
            'editValue' => [
                'class' => EditableColumnAction::className(),
                'modelClass' => Ad::className(),
                'outputValue' => function($model, $attribute, $key, $index) {
                    return ''.Ad::$enableMap[$model->$attribute];
                },
                'outputMessage' => function($model, $attribute, $key, $index) {
                    return '';
                },
                'showModelErrors' => true,
                'errorOptions' => ['header' => '']
            ],
        ]); // TODO: Change the autogenerated stub
    }

    /**
     * @inheritdoc
     */
    public function behaviors()
    {

        return [
            'access' => [
                'class' => AccessControl::className(),
                'only' => ['index', 'view', 'update', 'delete', 'create', 'upload',
                    'editValue',
                ],
                'rules' => [
                    [
                        'allow' => true,
                        'roles' => ['@'],
                    ],
                ],
            ],
            'verbs' => [
                'class' => VerbFilter::className(),
                'actions' => [
                    'delete' => ['POST'],
                ],
            ],
        ];
    }

    /**
     * Lists all Ad models.
     * @return mixed
     */
    public function actionIndex()
    {
        $searchModel = new AdSearch();
        $dataProvider = $searchModel->search(Yii::$app->request->queryParams);

        return $this->render('index', [
            'searchModel' => $searchModel,
            'dataProvider' => $dataProvider,
        ]);
    }

    /**
     * Displays a single Ad model.
     * @param integer $id
     * @return mixed
     */
    public function actionView($id)
    {
        return $this->render('view', [
            'model' => $this->findModel($id),
        ]);
    }

    /**
     * Creates a new Ad model.
     * If creation is successful, the browser will be redirected to the 'view' page.
     * @return mixed
     */
    public function actionCreate()
    {
        $model = new Ad();
        $model->scenario = 'insert';
        $adPositions = AdPosition::find()->asArray()->all();
        $adPositions = array_column($adPositions, 'position_name', 'position_id');

        $param = Yii::$app->request->post();
        if (Yii::$app->request->isPost) {
            $param['Ad']['end_time'] = DateTimeHelper::getFormatGMTTimesTimestamp($param['Ad']['end_time']);
            $param['Ad']['start_time'] = DateTimeHelper::getFormatGMTTimesTimestamp($param['Ad']['start_time']);
        }

        if ($model->load($param) && $model->save()) {
            return $this->redirect(['view', 'id' => $model->ad_id]);
        } else {
            return $this->render('create', [
                'model' => $model,
                'adPositions' => $adPositions,
            ]);
        }
    }

    /**
     * Updates an existing Ad model.
     * If update is successful, the browser will be redirected to the 'view' page.
     * @param integer $id
     * @return mixed
     */
    public function actionUpdate($id)
    {
        $model = $this->findModel($id);
        $model->scenario = 'update';
        $adPositions = AdPosition::find()->asArray()->all();
        $adPositions = array_column($adPositions, 'position_name', 'position_id');

        $param = Yii::$app->request->post();
        if (Yii::$app->request->isPost) {
            $param['Ad']['end_time'] = DateTimeHelper::getFormatGMTTimesTimestamp($param['Ad']['end_time']);
            $param['Ad']['start_time'] = DateTimeHelper::getFormatGMTTimesTimestamp($param['Ad']['start_time']);
        }
        if ($model->load($param, null) && $model->save()) {
            return $this->redirect(['view', 'id' => $model->ad_id]);
        } else {
            return $this->render('update', [
                'model' => $model,
                'adPositions' => $adPositions,
            ]);
        }
    }

    /**
     * Deletes an existing Ad model.
     * If deletion is successful, the browser will be redirected to the 'index' page.
     * @param integer $id
     * @return mixed
     */
    public function actionDelete($id)
    {
        $this->findModel($id)->delete();

        return $this->redirect(['index']);
    }

    /**
     * Finds the Ad model based on its primary key value.
     * If the model is not found, a 404 HTTP exception will be thrown.
     * @param integer $id
     * @return Ad the loaded model
     * @throws NotFoundHttpException if the model cannot be found
     */
    protected function findModel($id)
    {
        if (($model = Ad::findOne($id)) !== null) {
            return $model;
        } else {
            throw new NotFoundHttpException('The requested page does not exist.');
        }
    }
}
